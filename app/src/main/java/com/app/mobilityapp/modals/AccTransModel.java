package com.app.mobilityapp.modals;

public class AccTransModel {
    private String payModeStr;
    private String amountStr;
    private String creditAmountStr;
    private String dateStr;

    public String getPayModeStr() {
        return payModeStr;
    }

    public void setPayModeStr(String payModeStr) {
        this.payModeStr = payModeStr;
    }

    public String getAmountStr() {
        return amountStr;
    }

    public void setAmountStr(String amountStr) {
        this.amountStr = amountStr;
    }

    public String getCreditAmountStr() {
        return creditAmountStr;
    }

    public void setCreditAmountStr(String creditAmountStr) {
        this.creditAmountStr = creditAmountStr;
    }

    public String getDateStr() {
        return dateStr;
    }

    public void setDateStr(String dateStr) {
        this.dateStr = dateStr;
    }
}
